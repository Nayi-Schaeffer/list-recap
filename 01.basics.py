# crear una lista vacía
my_list = []

#Una propiedad importante de las listas es su largo

print(len(my_list))

#agregar elementos a un arreglo o lista

my_list.append(True)
my_list.append("Hola")
my_list.append(34)

print(my_list)

# La pricipal característica de un arreglo es que sus elementos estan ordenados por su possición partiendo desde cero

print(my_list[2])
print(my_list[0])
print(my_list[1])

#Que pasa con los indices negativos
print(my_list[-1])

print(len(my_list))

# Como conclusion podemos decir que el ultimo indice es su largo menos uno y se cuenta de atras hacia adelante.



